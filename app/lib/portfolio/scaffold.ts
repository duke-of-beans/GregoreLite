// ── Substitution helper ───────────────────────────────────────────────────────

/**
 * Replace {{key}} placeholders in a string with values from answers.
 * Unknown keys are replaced with an empty string.
 */
export function substituteAnswers(
  content: string,
  answers: Record<string, string>,
): string {
  return content.replace(/\{\{(\w+)\}\}/g, (_, key: string) => answers[key] ?? '');
}

// ── Date helper ───────────────────────────────────────────────────────────────

function today(): string {
  return new Date().toISOString().slice(0, 10);
}

