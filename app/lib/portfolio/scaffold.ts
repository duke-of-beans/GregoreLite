// ── Template registry ─────────────────────────────────────────────────────────

const TEMPLATES: Record<ProjectType, ScaffoldTemplate> = {
  code:     CODE_TEMPLATE,
  research: RESEARCH_TEMPLATE,
  business: BUSINESS_TEMPLATE,
  creative: CREATIVE_TEMPLATE,
  custom:   CUSTOM_TEMPLATE,
};

export function getScaffoldTemplate(type: ProjectType): ScaffoldTemplate {
  const template = TEMPLATES[type];
  if (!template) {
    throw new Error(`No scaffold template found for type: "${String(type)}"`);
  }
  return template;
}

// ── Scaffold execution ────────────────────────────────────────────────────────

/**
 * Create the project directory and write all template files.
 * answer substitution is applied to all content functions and strings.
 */
export function scaffoldProject(
  template: ScaffoldTemplate,
  dirPath: string,
  answers: Record<string, string>,
): ScaffoldResult {
  const filesCreated: string[] = [];

  try {
    if (!fs.existsSync(dirPath)) {
      fs.mkdirSync(dirPath, { recursive: true });
    }
  } catch (err) {
    return {
      success: false,
      filesCreated: [],
      error: `Failed to create directory: ${err instanceof Error ? err.message : String(err)}`,
    };
  }

  for (const file of template.files) {
    try {
      const rawContent = typeof file.content === 'function'
        ? file.content(answers)
        : substituteAnswers(file.content, answers);

      const filePath = path.join(dirPath, file.path);

      // Create parent directories if needed (e.g. nested files)
      const parentDir = path.dirname(filePath);
      if (!fs.existsSync(parentDir)) {
        fs.mkdirSync(parentDir, { recursive: true });
      }

      // Never overwrite an existing file (safe — user may have created files already)
      if (!fs.existsSync(filePath)) {
        fs.writeFileSync(filePath, rawContent, 'utf8');
        filesCreated.push(file.path);
      }
    } catch (err) {
      console.warn(`[portfolio/scaffold] Failed to write ${file.path}:`, err);
    }
  }

  return { success: true, filesCreated };
}

// ── Project registration ──────────────────────────────────────────────────────

/**
 * INSERT the new project into portfolio_projects and trigger an immediate scan.
 */
export function registerProject(
  dirPath: string,
  name: string,
  type: ProjectType,
  typeLabel?: string,
): string {
  const db = getDatabase();
  const id = `proj_${Date.now()}_${Math.random().toString(36).slice(2, 8)}`;
  const now = Date.now();

  db.prepare(`
    INSERT OR IGNORE INTO portfolio_projects
      (id, name, path, type, type_label, status, registered_at)
    VALUES (?, ?, ?, ?, ?, 'active', ?)
  `).run(id, name.trim(), dirPath, type, typeLabel ?? null, now);

  // Trigger immediate scan (non-blocking)
  void Promise.resolve().then(() => {
    try {
      scanSingleProject(dirPath);
    } catch (err) {
      console.warn('[portfolio/scaffold] Post-registration scan failed:', err);
    }
  });

  return id;
}
