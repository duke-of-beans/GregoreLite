/**
 * Portfolio Scaffold — Sprint 26.0
 *
 * Scaffold templates for each project type. Creates a full directory of
 * tracking files from onboarding answers. Registers the new project in SQLite.
 *
 * Public API:
 *   getScaffoldTemplate(type)                      — returns the template for a type
 *   scaffoldProject(template, dirPath, answers)    — creates directory + files
 *   registerProject(dirPath, name, type, label?)   — INSERT into portfolio_projects
 *   substituteAnswers(content, answers)            — {{variable}} substitution helper
 *
 * RULE: All file paths are relative within the new project directory.
 * RULE: No LLM calls here — templates are deterministic from answers.
 */

